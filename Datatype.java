package com.javapractice;

public class Datatype {
	
	public static  void main(String [] args) {
		
		byte byteMethod = -127;
		System.out.println(byteMethod);

		short shortMethod = 32767;
		System.out.println(shortMethod);

		
		int intMethod = 214748364;
		System.out.println(intMethod);
		
		double longMethod = -9.22337204;
		System.out.println(longMethod);

		float floatMethod = -2.14748365f;
		System.out.println(floatMethod);

		double doubleMethod = 9.22337204;
		System.out.println(doubleMethod);

		char charMethod = 'A';
		System.out.println(charMethod);

		boolean booleanMethod = true;
		System.out.println(booleanMethod);

		
		
	}

}
